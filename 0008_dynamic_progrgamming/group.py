class SocialGroup:

    @staticmethod
    def generate_random_group():
        random_index = random.randint(0, len(words_list)-1)
        random_click = random.randint(100, 50_000)
        random_price = random.randint(100, 10_000)
        return SocialGroup(
            words_list[random_index],
            random_click,
            random_price
        )

    def __init__(self, name, clicks, price):
        self.__name = name
        self.__clicks = clicks
        self.__price = price

    def __str__(self):
        return self.__name + "<clicks:" + str(self.__clicks) + ",price:" + str(self.__price) + ">"

    def __repr__(self):
        return self.__str__()

    def get_clicks(self):
        return self.__clicks

    def get_price(self):
        return self.__price